package condominio.Jair.parte2.visitante;

public class Visitante {
	private String funcao;
	private String nome;
	
	
	
	Visitante(String funcao, String nome) {
		this.funcao = funcao;
		this.nome = nome;
	}
	public String getFuncao() {
		return funcao;
	}
	public String getNome() {
		return nome;
	}
	
	
	

}
